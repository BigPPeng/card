class Solution {
    public int[] intersect(int[] nums1, int[] nums2) {
        Arrays.sort(nums1);
        Arrays.sort(nums2);

        int min = Math.min(nums1.length,nums2.length);
        int[] res = new int[min];
        int k = 0;

        int i = 0;
        int j = 0;
        while (i < nums1.length && j< nums2.length) {
            int i1 = nums1[i];
            int i2 = nums2[j];
            if (i1 == i2) {
                res[k++] = i1;
                i++;
                j++;
                continue;
            }
            if (i1 < i2) {
                i++;
            } else {
                j++;
            }
        }
        return Arrays.copyOfRange(res, 0, k);
    }
}
